String? colorToString(MaterialColor color) {
  if (color == cannabisColorMat) {
    return 'Green';
  } else if (color == researchColorMat) {
    return 'Blue';
  } else if (color == empathogenColorMat) {
    return 'Red';
  } else if (color == depressantColorMat) {
    return 'Yellow';
  } else if (color == habitColorMat) {
    return 'Orange';
  } else if (color == psychColorMat) {
    return 'Purple';
  } else {
    return null;
  }
}

MaterialColor stringToColor(String color) {
  switch (color) {
    case 'Green':
      return cannabisColorMat;

    case 'Blue':
      return researchColorMat;

    case 'Red':
      return empathogenColorMat;

    case 'Yellow':
      return depressantColorMat;

    case 'Orange':
      return habitColorMat;

    case 'Purple':
      return psychColorMat;

    default:
      return cannabisColorMat;
  }
}

